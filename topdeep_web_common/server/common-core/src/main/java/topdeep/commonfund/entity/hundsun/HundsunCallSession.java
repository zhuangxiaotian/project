package topdeep.commonfund.entity.hundsun;

/**
 * @author niexin
 *
 */
public class HundsunCallSession extends HundsunBaseInput {
	private String merPassword;

	private String idType;

	private String idNo;

	private String password;

	/**
	 * @return the merPassword
	 */
	public String getMerPassword() {
		return merPassword;
	}

	/**
	 * @param merPassword the merPassword to set
	 */
	public void setMerPassword(String merPassword) {
		this.merPassword = merPassword;
	}

	/**
	 * @return the idType
	 */
	public String getIdType() {
		return idType;
	}

	/**
	 * @param idType the idType to set
	 */
	public void setIdType(String idType) {
		this.idType = idType;
	}

	/**
	 * @return the idNo
	 */
	public String getIdNo() {
		return idNo;
	}

	/**
	 * @param idNo the idNo to set
	 */
	public void setIdNo(String idNo) {
		this.idNo = idNo;
	}

	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}

	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
}
